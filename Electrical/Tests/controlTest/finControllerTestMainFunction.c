/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SPI2_Init();
  MX_TIM4_Init();
  /* USER CODE BEGIN 2 */

  // start PWM channels 1, 2, and 3 on timer 4
  HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_2);
  HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_3);

  htim4.Instance->CCR1 = 0;
  htim4.Instance->CCR2 = 0;
  htim4.Instance->CCR3 = 0;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  // set slave select to the opposite of the slave select pin (the stm32 is selected if the slave select pin is low)
	  slaveSelect = !(HAL_GPIO_ReadPin(SS_GPIO_Port, SS_Pin));

	  // get state of SPI
	  spiState = HAL_SPI_GetState(&hspi2);

	  // read SPI if SPI is ready and the slave select pin if pulled low
	  if (spiState == HAL_SPI_STATE_READY && slaveSelect)
	  {
		  // read 4 bytes from SPI with 50 millisecond timeout (only 3 bytes are being sent so it will always timeout)
		  spiStatus = HAL_SPI_Receive(&hspi2, commands, 4, 50);

		  // set the fin angles from the command
		  setFins(commands);
	  }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

